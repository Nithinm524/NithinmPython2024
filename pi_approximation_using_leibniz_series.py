def leibniz_pi(terms):
    pi_estimate = 0
    for k in range(terms):
        pi_estimate += (-1) ** k / (2 * k + 1)
    return 4 * pi_estimate

# Example usage
terms = int(input("Enter the number of terms: "))
print(f"Approximated value of Ï€: {leibniz_pi(terms)}")